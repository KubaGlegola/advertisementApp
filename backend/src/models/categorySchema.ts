import mongoose, { Schema } from "mongoose";
const categorySchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  slug: {
    type: String,
    required: true,
    unique: true,
  },
  image: {
    type: String,
    required: true,
  },
});

export default mongoose.model("Category", categorySchema);
